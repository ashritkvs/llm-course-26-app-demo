def _llm_call(llm: ChatGroq, prompt: str) -> str:
    return llm.invoke(prompt).content.strip()

def _limit_data(data, limit=10):
    """Truncate lists to prevent exceeding LLM token limits."""
    if isinstance(data, list):
        return data[:limit]
    if isinstance(data, dict):
        return {k: _limit_data(v, limit) for k, v in data.items()}
    return data


