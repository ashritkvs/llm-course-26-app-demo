def build_json_summary(target: str, recon: dict, vulns_data: dict,
                       exploit_data: dict, scan_date: str) -> dict:
    vulns     = vulns_data.get("vulnerabilities", [])
    confirmed = [r for r in exploit_data.get("results", []) if r.get("status") == "CONFIRMED"]

    return {
        "report_metadata": {
            "target":       target,
            "generated_at": scan_date,
            "generator":    "pentest-agent/v1.0",
        },
        "scope": {
            "subdomains_found": len(recon.get("subdomains", [])),
            "open_ports":       [p.get("port") for p in recon.get("open_ports", [])],
            "technologies":     list(set(recon.get("technologies", []))),
            "ip_addresses":     recon.get("dns_records", {}).get("A", []),
        },
        "vulnerability_summary": vulns_data.get("summary", {}),
        "total_vulnerabilities": len(vulns),
        "confirmed_exploitable": len(confirmed),
        "top_vulnerabilities": [
            {
                "title":      v.get("title"),
                "severity":   v.get("severity"),
                "cvss_score": v.get("cvss_score"),
                "category":   v.get("category"),
                "target_url": v.get("target_url"),
                "remediation":v.get("remediation"),
            }
            for v in sorted(vulns, key=lambda x: -x.get("cvss_score", 0))[:10]
        ],
        "confirmed_exploits": [
            {
                "title":    r.get("vuln_title"),
                "category": r.get("vuln_category"),
                "technique":r.get("technique"),
                "impact":   r.get("impact"),
                "severity": r.get("severity"),
            }
            for r in confirmed
        ],
        "overall_risk": _compute_risk(vulns_data.get("summary", {}), len(confirmed)),
    }


def _compute_risk(summary: dict, confirmed: int) -> str:
    if summary.get("CRITICAL", 0) > 0 or confirmed > 0:
        return "CRITICAL"
    if summary.get("HIGH", 0) >= 3:
        return "HIGH"
    if summary.get("HIGH", 0) > 0 or summary.get("MEDIUM", 0) >= 5:
        return "MEDIUM"
    return "LOW"


