def run(recon: dict, vuln_data: dict, exploit_data: dict,
        model: str = GROQ_MODEL_DEFAULT) -> dict:
    """
    Entry point called by main.py.
    Accepts dicts from Parts 1, 2, 3.
    Returns dict with paths to all generated output files.
    """
    target    = recon.get("target", vuln_data.get("target", "unknown"))
    scan_date = datetime.datetime.utcnow().strftime("%Y-%m-%d %H:%M UTC")

    vulns     = vuln_data.get("vulnerabilities", [])
    vuln_sum  = vuln_data.get("summary", {})
    if not isinstance(vuln_sum, dict):
        # Fallback if summary is a string (like strategic summary) or missing
        from agents.report_writer import _get_summary_counts
        vuln_sum = _get_summary_counts(vulns)
        
    results   = exploit_data.get("results", [])
    confirmed = [r for r in results if r.get("status") == "CONFIRMED"]

    scan_dates = {
        "recon":   recon.get("timestamp", "N/A")[:10],
        "vuln":    vuln_data.get("scan_start", "N/A")[:10],
        "exploit": exploit_data.get("session_start", "N/A")[:10],
    }

    console.print(Panel.fit(
        f"[bold green]Part 4 — Report Writer[/bold green]\n"
        f"[dim]Target: {target}  |  Model: {model}[/dim]",
        border_style="green"
    ))

    # Initialize LLM based on configured provider
    if LLM_PROVIDER == "gemini":
        primary_llm = ChatOpenAI(
            api_key=GEMINI_API_KEY,
            base_url="https://generativelanguage.googleapis.com/v1beta/openai/",
            model=GEMINI_MODEL_DEFAULT,
            temperature=0.2
        )
        fallback_llm = ChatGroq(
            model=model,
            api_key=GROQ_API_KEY,
            temperature=0.2,
            max_tokens=4096
        )
        llm = primary_llm.with_fallbacks([fallback_llm])
        console.print(f"[dim]Using LLM: Gemini ({GEMINI_MODEL_DEFAULT}) with Groq Fallback[/dim]")
            
    else:  # groq default
        primary_llm = ChatGroq(
            model=model,
            api_key=GROQ_API_KEY,
            temperature=0.2,
            max_tokens=4096
        )
        # Use OpenAI-compatible endpoint for Gemini to avoid package conflicts
        fallback_llm = ChatOpenAI(
            api_key=GEMINI_API_KEY,
            base_url="https://generativelanguage.googleapis.com/v1beta/openai/",
            model=GEMINI_MODEL_DEFAULT,
            temperature=0.2
        )
        llm = primary_llm.with_fallbacks([fallback_llm])
        console.print(f"[dim]Using LLM: Groq ({model}) with Gemini (OpenAI-mode) Fallback[/dim]")

    # ── Generate all 7 sections ───────────────────────────
    sections = {}

    console.print("\n[bold]Generating report sections...[/bold]")

    console.print("  [cyan]1/7[/cyan] Executive summary...")
    sections["executive_summary"] = section_executive_summary(
        llm, target, vuln_sum, len(confirmed), scan_dates)
    time.sleep(2)  # Delay to avoid rate limiting

    console.print("  [cyan]2/7[/cyan] Attack surface...")
    sections["attack_surface"] = section_attack_surface(llm, recon)
    time.sleep(2)

    console.print("  [cyan]3/7[/cyan] Severity table...")
    sections["severity_table"] = section_severity_table(vuln_sum, vulns)
    # No LLM call for severity table, but add delay before next LLM call
    time.sleep(2)

    console.print("  [cyan]4/7[/cyan] Critical & high findings...")
    sections["critical_findings"] = section_critical_findings(llm, vulns, results)
    time.sleep(2)

    console.print("  [cyan]5/7[/cyan] Medium & low findings...")
    sections["medium_low"] = section_medium_low(llm, vulns)
    time.sleep(2)

    console.print("  [cyan]6/7[/cyan] Attack chain analysis...")
    sections["attack_chain"] = section_attack_chain(llm, confirmed, vulns, target)
    time.sleep(2)

    console.print("  [cyan]7/8[/cyan] Remediation plan + risk rating...")
    sections["remediation"] = section_remediation_plan(llm, vulns, confirmed)
    time.sleep(1)
    sections["risk_rating"]  = section_risk_rating(llm, vuln_sum, len(confirmed), target)
    time.sleep(1)

    console.print("  [cyan]8/8[/cyan] Backdoor & compromise assessment...")
    sections["backdoor_analysis"] = section_backdoor_analysis(llm, target, vulns)

    # ── Assemble full markdown report ─────────────────────
    ts_safe = datetime.datetime.utcnow().strftime("%Y%m%d_%H%M%S")
    safe_t  = target.replace("/","_").replace(":","_").replace(".","_")

    full_markdown = f"""# Penetration Test Report — {target}

**Generated:** {scan_date}
**Classification:** CONFIDENTIAL
**Tool:** Pentest Agent v1.0

---

## Executive Summary

{sections['executive_summary']}

---

{sections['attack_surface']}

---

{sections['severity_table']}

---

{sections['critical_findings']}

---

{sections['medium_low']}

---

{sections['attack_chain']}

---

{sections['backdoor_analysis']}

---

{sections['remediation']}

---

{sections['risk_rating']}

---

*This report was generated autonomously by Pentest Agent.
All findings should be verified by a qualified security professional before remediation.*
"""

    # ── Save all output formats ───────────────────────────
    FINAL_DIR.mkdir(parents=True, exist_ok=True)

    md_path   = FINAL_DIR / f"report_{safe_t}_{ts_safe}.md"
    html_path = FINAL_DIR / f"report_{safe_t}_{ts_safe}.html"
    json_path = FINAL_DIR / f"report_{safe_t}_{ts_safe}.json"

    md_path.write_text(full_markdown, encoding="utf-8")
    console.print(f"\n[green]✓[/green] Markdown:  {md_path}")

    html_content = render_html(target, full_markdown, vuln_sum,
                               len(confirmed), scan_date)
    html_path.write_text(html_content, encoding="utf-8")
    console.print(f"[green]✓[/green] HTML:      {html_path}")

    json_summary = build_json_summary(target, recon, vuln_data, exploit_data, scan_date)
    json_path.write_text(json.dumps(json_summary, indent=2), encoding="utf-8")
    console.print(f"[green]✓[/green] JSON:      {json_path}")

    # ── Console preview ───────────────────────────────────
    console.print("\n" + "═" * 60)
    console.print(Panel(
        sections["executive_summary"],
        title=f"[bold green]Executive Summary — {target}[/bold green]",
        border_style="green",
    ))

    # Risk rating table
    overall_risk = json_summary["overall_risk"]
    risk_color   = {"CRITICAL":"bold red","HIGH":"red","MEDIUM":"yellow","LOW":"green"}.get(overall_risk,"white")
    table = Table(title="Final Risk Assessment", show_header=True)
    table.add_column("Category",  style="bold")
    table.add_column("Result")
    table.add_row("Target",                target)
    table.add_row("Total vulnerabilities", str(len(vulns)))
    table.add_row("Confirmed exploitable", str(len(confirmed)))
    table.add_row("Critical findings",     str(vuln_sum.get("CRITICAL",0)))
    table.add_row("High findings",         str(vuln_sum.get("HIGH",0)))
    table.add_row("Overall risk",          f"[{risk_color}]{overall_risk}[/{risk_color}]")
    console.print(table)

    return {
        "target":       target,
        "overall_risk": overall_risk,
        "md_path":      str(md_path),
        "html_path":    str(html_path),
        "json_path":    str(json_path),
        "summary":      json_summary,
    }


