def render_html(target: str, markdown_content: str, vuln_summary: dict,
                confirmed_count: int, scan_date: str) -> str:
    crit  = vuln_summary.get("CRITICAL", 0)
    high  = vuln_summary.get("HIGH", 0)
    med   = vuln_summary.get("MEDIUM", 0)
    low   = vuln_summary.get("LOW", 0)
    total = sum(vuln_summary.values())

    # Full markdown → HTML conversion
    import re

    def inline_format(text: str) -> str:
        """Apply inline markdown formatting: bold, italic, inline code, links."""
        # Bold+italic combined: ***text***
        text = re.sub(r'\*\*\*(.+?)\*\*\*', r'<strong><em>\1</em></strong>', text)
        # Bold: **text**
        text = re.sub(r'\*\*(.+?)\*\*', r'<strong>\1</strong>', text)
        # Italic: *text* or _text_
        text = re.sub(r'\*([^*\n]+?)\*', r'<em>\1</em>', text)
        text = re.sub(r'_([^_\n]+?)_', r'<em>\1</em>', text)
        # Inline code: `text`
        text = re.sub(r'`([^`]+?)`', r'<code>\1</code>', text)
        # Strikethrough: ~~text~~
        text = re.sub(r'~~(.+?)~~', r'<del>\1</del>', text)
        # Severity badges
        text = re.sub(r'\bCRITICAL\b', '<span class="badge crit">CRITICAL</span>', text)
        text = re.sub(r'\bHIGH\b', '<span class="badge high">HIGH</span>', text)
        text = re.sub(r'\bMEDIUM\b', '<span class="badge med">MEDIUM</span>', text)
        text = re.sub(r'\b(LOW)\b(?! risk)', '<span class="badge low">LOW</span>', text)
        return text

    def md_to_html(md: str) -> str:
        lines = md.split("\n")
        html = []
        in_table = False
        in_ul = False
        in_ol = False
        in_code_block = False
        code_lines = []

        def close_lists():
            nonlocal in_ul, in_ol
            if in_ul:
                html.append("</ul>")
                in_ul = False
            if in_ol:
                html.append("</ol>")
                in_ol = False

        def close_table():
            nonlocal in_table
            if in_table:
                html.append("</tbody></table>")
                in_table = False

        for line in lines:
            # Fenced code blocks
            if line.strip().startswith("```"):
                if in_code_block:
                    html.append("<pre><code>" + "\n".join(code_lines) + "</code></pre>")
                    code_lines = []
                    in_code_block = False
                else:
                    close_lists()
                    close_table()
                    in_code_block = True
                continue
            if in_code_block:
                code_lines.append(line)
                continue

            # Tables
            if line.startswith("|"):
                close_lists()
                if not in_table:
                    html.append('<table><thead>')
                    in_table = True
                    cells = [inline_format(c.strip()) for c in line.strip("|").split("|")]
                    html.append("<tr>" + "".join(f"<th>{c}</th>" for c in cells) + "</tr>")
                    html.append("</thead><tbody>")
                elif re.match(r"^\|[-| :]+\|$", line):
                    pass  # separator row — skip
                else:
                    cells = [inline_format(c.strip()) for c in line.strip("|").split("|")]
                    html.append("<tr>" + "".join(f"<td>{c}</td>" for c in cells) + "</tr>")
                continue
            else:
                close_table()

            # Horizontal rule
            if re.match(r'^---+$', line.strip()):
                close_lists()
                html.append("<hr>")
                continue

            # Headings
            if line.startswith("#### "):
                close_lists()
                html.append(f"<h4>{inline_format(line[5:])}</h4>")
            elif line.startswith("### "):
                close_lists()
                html.append(f"<h3>{inline_format(line[4:])}</h3>")
            elif line.startswith("## "):
                close_lists()
                html.append(f"<h2>{inline_format(line[3:])}</h2>")
            elif line.startswith("# "):
                close_lists()
                html.append(f"<h1>{inline_format(line[2:])}</h1>")
            # Blockquote
            elif line.startswith("> "):
                close_lists()
                html.append(f"<blockquote><p>{inline_format(line[2:])}</p></blockquote>")
            # Unordered list
            elif re.match(r"^[-*+] ", line):
                close_table()
                if in_ol:
                    html.append("</ol>")
                    in_ol = False
                if not in_ul:
                    html.append("<ul>")
                    in_ul = True
                content = inline_format(line[2:])
                html.append(f"<li>{content}</li>")
            # Ordered list
            elif re.match(r"^\d+\. ", line):
                close_table()
                if in_ul:
                    html.append("</ul>")
                    in_ul = False
                if not in_ol:
                    html.append("<ol>")
                    in_ol = True
                content = re.sub(r"^\d+\. ", "", line)
                html.append(f"<li>{inline_format(content)}</li>")
            # Empty line
            elif not line.strip():
                close_lists()
                html.append("<br>")
            # Normal paragraph
            else:
                close_lists()
                content = inline_format(line)
                html.append(f"<p>{content}</p>")

        close_table()
        close_lists()
        if in_code_block and code_lines:
            html.append("<pre><code>" + "\n".join(code_lines) + "</code></pre>")

        return "\n".join(html)

    body = md_to_html(markdown_content)

